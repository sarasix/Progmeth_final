package map;

public class putMap {
	private int indexX,indexY;
	public putMap(int indexX,int indexY) {
		this.indexX = indexX;
		this.indexY = indexY;
	}
	public int getIndexX() {
		return this.indexX;
	}
	public int getIndexY() {
		return this.indexY;
	}
	public void setIndexX(int indexX) {
		this.indexX=indexX;
	}
	public void setIndexY(int indexY) {
		this.indexY=indexY;
	}

}
