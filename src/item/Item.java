package logic;

public class Item {
	private int indexX;
	private int indexY;
	public Item(int indexX,int indexY) {
		this.indexX=indexX;
		this.indexY=indexY;
	}
	public int getIndexX() {
		return indexX;
	}
	public int getIndexY() {
		return indexY;
	}
	public void setIndexX(int indexX) {
		this.indexX=indexX;
	}
	public void setIndexY(int indexY) {
		this.indexY=indexY;
	}

}
